PROTO_EXAMPLE = """
syntax = "proto3";                     // proto3 is the modern default

// Package prevents name collisions when importing across services
package order.v2;

// Go package path (generated code configuration)
option go_package = "github.com/example/order/gen/go";

import "google/protobuf/timestamp.proto";   // Well-known types
import "google/protobuf/any.proto";         // Embed arbitrary message types

// ─── Scalar types available in proto3 ────────────────────────────────────────
// double, float                → Python float
// int32, int64                 → Python int  (negative values waste space; use sint32/sint64)
// uint32, uint64               → Python int  (always non-negative)
// sint32, sint64               → Python int  (efficient for negative numbers; uses zigzag encoding)
// fixed32, fixed64             → Python int  (constant 4/8 bytes; good for ids > 2^28)
// bool                         → Python bool
// string                       → Python str  (must be valid UTF-8)
// bytes                        → Python bytes (arbitrary binary)

// ─── Message definition ───────────────────────────────────────────────────────
message OrderItem {
    string sku = 1;              // Field number 1 — THE WIRE IDENTITY. Never reuse.
    int32 quantity = 2;          // If you remove this field, reserve the number: reserved 2;
    double unit_price_usd = 3;
}

message CreateOrderRequest {
    int64 customer_id = 1;
    repeated OrderItem items = 2;   // 'repeated' = list; zero items allowed in proto3

    // 'oneof' — at most one of these fields can be set at a time
    oneof delivery_preference {
        string delivery_address = 3;
        int64 pickup_location_id = 4;
    }

    map<string, string> metadata = 5;   // String-keyed map; value can be any type

    google.protobuf.Timestamp requested_delivery = 6;  // Structured timestamp
}

enum OrderStatus {
    ORDER_STATUS_UNSPECIFIED = 0;   // proto3 REQUIRES enum value 0 (default)
    ORDER_STATUS_PENDING = 1;
    ORDER_STATUS_CONFIRMED = 2;
    ORDER_STATUS_SHIPPED = 3;
    ORDER_STATUS_DELIVERED = 4;
    ORDER_STATUS_CANCELLED = 5;
}

message OrderResponse {
    string order_id = 1;
    OrderStatus status = 2;
    int64 customer_id = 3;
    repeated OrderItem items = 4;
    google.protobuf.Timestamp created_at = 5;
    google.protobuf.Any extension_data = 6;  // Escape hatch for polymorphic payloads

    // Fields reserved so they can never be accidentally reused
    reserved 7, 8;                 // reserve by field number
    reserved "old_field_name";     // reserve by name
}

// ─── Service definition ────────────────────────────────────────────────────────
service OrderService {
    // Unary: one request, one response
    rpc CreateOrder (CreateOrderRequest) returns (OrderResponse);

    // Server streaming: one request, stream of responses
    rpc WatchOrderStatus (WatchOrderRequest) returns (stream OrderStatusUpdate);

    // Client streaming: stream of requests, one response
    rpc BatchCreateOrders (stream CreateOrderRequest) returns (BatchOrderResponse);

    // Bidirectional streaming: stream ↔ stream
    rpc OrderChat (stream ChatMessage) returns (stream ChatMessage);
}
"""

