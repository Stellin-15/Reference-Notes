def serve(port: int = 50051, max_workers: int = 10) -> None:
    """
    Start the gRPC server.
    ThreadPoolExecutor is the default; for async use grpc.aio.server().
    """
    # Create a server with a thread pool
    server = grpc.server(
        futures.ThreadPoolExecutor(max_workers=max_workers),
        # Interceptors run before/after each RPC — good for auth, logging, tracing
        interceptors=[],
        options=[
            # Maximum message size to receive (default 4MB)
            ("grpc.max_receive_message_length", 16 * 1024 * 1024),  # 16 MB
            # Maximum message size to send
            ("grpc.max_send_message_length", 16 * 1024 * 1024),
            # Keepalive: send ping after 30s of inactivity
            ("grpc.keepalive_time_ms", 30_000),
            # Close connection if no ping ack within 10s
            ("grpc.keepalive_timeout_ms", 10_000),
        ],
    )

    # Register the servicer — in real code:
    # order_service_pb2_grpc.add_OrderServiceServicer_to_server(OrderServiceServicer(), server)

    # Bind to port; insecure for dev, TLS for production
    server.add_insecure_port(f"[::]:{port}")

    # For production TLS:
    # with open("server.key", "rb") as f: private_key = f.read()
    # with open("server.crt", "rb") as f: cert_chain = f.read()
    # credentials = grpc.ssl_server_credentials([(private_key, cert_chain)])
    # server.add_secure_port(f"[::]:{port}", credentials)

    server.start()
    logger.info("gRPC server listening on port %d", port)

    try:
        server.wait_for_termination()
    except KeyboardInterrupt:
        # Graceful shutdown: finish in-flight RPCs, then stop
        server.stop(grace=5)  # 5 seconds grace period


