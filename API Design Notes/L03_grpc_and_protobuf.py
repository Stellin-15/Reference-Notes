# PART 2 — gRPC SERVER IMPLEMENTATION
# =============================================================================
# The servicer class inherits from the generated *Servicer base class.
# Each RPC method receives a context object carrying metadata, deadline, etc.
# =============================================================================

class OrderServiceServicer:
    """
    Concrete implementation of the OrderService gRPC service.
    In a real project this inherits from order_service_pb2_grpc.OrderServiceServicer.
    The generated base class provides default implementations that return
    UNIMPLEMENTED for every method — you override only what you support.
    """

    # ── Unary RPC ──────────────────────────────────────────────────────────────
    def CreateOrder(self, request, context):
        """
        request: CreateOrderRequest protobuf message
        context: grpc.ServicerContext — abort, set_trailing_metadata, etc.
        """
        logger.info("CreateOrder called for customer_id=%s", request.customer_id)

        # Validate input; if invalid, abort with a gRPC error code
        if not request.items:
            # INVALID_ARGUMENT (3) = client sent bad data; client should not retry
            context.abort(
                grpc.StatusCode.INVALID_ARGUMENT,
                "At least one item is required",
            )
            return  # abort() does not stop execution — must return explicitly

        # Check if the deadline has already passed before doing expensive work
        if context.time_remaining() is not None and context.time_remaining() < 0.1:
            context.abort(grpc.StatusCode.DEADLINE_EXCEEDED, "Deadline already elapsed")
            return

        # Simulate DB write
        order_id = f"ORD-{int(time.time())}"

        # Attach trailing metadata (analogous to HTTP response headers)
        context.set_trailing_metadata([
            ("x-request-id", order_id),
            ("x-region", "us-west-2"),
        ])

        # Return a response message (would be order_service_pb2.OrderResponse)
        return {"order_id": order_id, "status": 1, "customer_id": request.customer_id}

    # ── Server-streaming RPC ───────────────────────────────────────────────────
    def WatchOrderStatus(self, request, context) -> Iterator[dict]:
        """
        Yields multiple responses. The client receives each as it's sent.
        Use case: long-lived watch; push status updates as they happen.
        """
        order_id = request.order_id  # type: ignore[attr-defined]
        statuses = ["PENDING", "CONFIRMED", "SHIPPED", "DELIVERED"]

        for status in statuses:
            # Check if the client cancelled the stream (they closed the connection)
            if not context.is_active():
                logger.info("Client cancelled WatchOrderStatus stream")
                return

            yield {"order_id": order_id, "status": status}
            time.sleep(1)  # In prod: sleep until a DB change-event arrives

    # ── Client-streaming RPC ───────────────────────────────────────────────────
    def BatchCreateOrders(self, request_iterator, context) -> dict:
        """
        Iterates over a stream of CreateOrderRequest messages from the client.
        Returns a single summary response when the stream is exhausted.
        Use case: bulk import, chunked file uploads.
        """
        created_ids = []
        errors = []

        for i, request in enumerate(request_iterator):
            try:
                # Process each request as it arrives
                order_id = f"ORD-BATCH-{i}"
                created_ids.append(order_id)
            except Exception as e:
                errors.append(str(e))

        return {"created_order_ids": created_ids, "error_count": len(errors)}

    # ── Bidirectional streaming RPC ────────────────────────────────────────────
    def OrderChat(self, request_iterator, context) -> Iterator[dict]:
        """
        Receives messages from the client stream and yields responses.
        Both sides send independently — full-duplex.
        Use case: real-time driver ↔ passenger messaging.
        """
        for message in request_iterator:
            if not context.is_active():
                break
            # Echo back with a timestamp — real impl would route to other party
            yield {
                "sender": "system",
                "content": f"Received: {message.content}",  # type: ignore[attr-defined]
                "timestamp": int(time.time()),
            }


