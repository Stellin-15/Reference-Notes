# PART 6 — INTERCEPTORS (middleware for gRPC)
# =============================================================================
# Interceptors wrap every RPC call. Use for: auth, logging, metrics, tracing.
# Client-side and server-side interceptors are registered separately.
# =============================================================================

class LoggingClientInterceptor(grpc.UnaryUnaryClientInterceptor):
    """
    Logs every outgoing unary RPC with timing.
    For streaming RPCs you'd implement UnaryStreamClientInterceptor,
    StreamUnaryClientInterceptor, or StreamStreamClientInterceptor.
    """

    def intercept_unary_unary(self, continuation, client_call_details, request):
        method = client_call_details.method  # e.g., "/order.v2.OrderService/CreateOrder"
        start = time.monotonic()
        logger.info("→ gRPC %s", method)

        try:
            response = continuation(client_call_details, request)
            elapsed = time.monotonic() - start
            logger.info("← gRPC %s completed in %.3fs", method, elapsed)
            return response
        except grpc.RpcError as e:
            elapsed = time.monotonic() - start
            logger.error("← gRPC %s failed [%s] in %.3fs", method, e.code(), elapsed)
            raise


class AuthServerInterceptor(grpc.ServerInterceptor):
    """
    Validates bearer tokens on every incoming RPC.
    Returns UNAUTHENTICATED if token is missing or invalid.
    """

    def __init__(self, valid_tokens: set[str]):
        self._valid_tokens = valid_tokens

    def intercept_service(self, continuation, handler_call_details):
        # Extract metadata from incoming call
        metadata = dict(handler_call_details.invocation_metadata)
        auth_header = metadata.get("authorization", "")

        if not auth_header.startswith("Bearer "):
            # Return an abort handler; the actual RPC method is never called
            def abort(request, context):
                context.abort(grpc.StatusCode.UNAUTHENTICATED, "Missing bearer token")
            return grpc.unary_unary_rpc_method_handler(abort)

        token = auth_header[len("Bearer "):]
        if token not in self._valid_tokens:
            def abort(request, context):
                context.abort(grpc.StatusCode.UNAUTHENTICATED, "Invalid token")
            return grpc.unary_unary_rpc_method_handler(abort)

        # Token is valid; let the actual handler proceed
        return continuation(handler_call_details)


