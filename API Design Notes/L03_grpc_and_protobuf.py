import asyncio


class AsyncOrderServiceServicer:
    """
    Async servicer — inherits from the grpc.aio generated base.
    All methods are coroutines (async def).
    """

    async def CreateOrder(self, request, context: grpc.aio.ServicerContext):
        """Async unary RPC handler."""
        # Await DB writes without blocking the event loop
        await asyncio.sleep(0)  # Placeholder for actual async DB call
        return {"order_id": "ORD-ASYNC-001", "status": 1}

    async def WatchOrderStatus(self, request, context: grpc.aio.ServicerContext):
        """Async server-streaming RPC. Use 'yield' to send each response."""
        for status in ["PENDING", "CONFIRMED", "DELIVERED"]:
            if context.cancelled():
                return
            yield {"status": status}
            await asyncio.sleep(1)  # Non-blocking sleep


