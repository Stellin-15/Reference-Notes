def create_channel(host: str = "localhost", port: int = 50051) -> grpc.Channel:
    """
    Create a reusable gRPC channel. ONE channel per target; share across threads.
    Channels are thread-safe and maintain a connection pool internally.
    """
    # Insecure (dev/internal):
    channel = grpc.insecure_channel(
        f"{host}:{port}",
        options=[
            # Initial backoff for retry (default 1s)
            ("grpc.initial_reconnect_backoff_ms", 500),
            # Maximum backoff cap
            ("grpc.max_reconnect_backoff_ms", 5_000),
        ],
    )

    # Wait until the channel is ready (or timeout)
    try:
        grpc.channel_ready_future(channel).result(timeout=10)
        logger.info("Channel connected to %s:%d", host, port)
    except grpc.FutureTimeoutError:
        logger.error("Could not connect to gRPC server within 10 seconds")
        raise

    return channel


