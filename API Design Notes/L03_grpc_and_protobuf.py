# =============================================================================
# WHAT: gRPC and Protocol Buffers (protobuf) — Python Reference
# WHY:  gRPC gives you typed, high-performance RPC with streaming built in.
#       Protobuf serializes 3–10x smaller than JSON and 10–100x faster to
#       parse. The combination dominates internal service communication at
#       Google, Netflix, Uber, and most large microservice platforms.
# LEVEL: Intermediate → Advanced
# =============================================================================
#
# CONCEPT OVERVIEW
# ----------------
# Protocol Buffers (protobuf) is a language-neutral binary serialization format.
# You define message types in .proto files, then compile them to Python (or any
# other language) with protoc. The generated code handles encoding/decoding.
#
# gRPC is an RPC framework that uses:
#   - HTTP/2 as the transport (multiplexing, header compression, push)
#   - Protobuf as the default serialization format
#   - A .proto service definition as the contract
#
# Four call types:
#   Unary             — single request, single response (like HTTP)
#   Server streaming  — single request, stream of responses
#   Client streaming  — stream of requests, single response
#   Bidirectional     — stream of requests, stream of responses (chat, telemetry)
#
# PRODUCTION USE CASE
# -------------------
# A ride-sharing platform uses gRPC internally:
#   - Driver location service: client-streaming (driver sends GPS pings)
#   - ETA service: unary (request trip route, get back ETA)
#   - Surge pricing feed: server-streaming (clients receive pricing updates)
#   - Live tracking: bidirectional (driver ↔ passenger real-time updates)
# The HTTP/JSON API exposed to mobile apps is transcoded through grpc-gateway.
#
# COMMON MISTAKES
# ---------------
# 1. Changing a field number → permanently breaks wire compatibility
# 2. Using gRPC status code 2 (UNKNOWN) for app errors → use specific codes
# 3. Not setting deadlines on every client call → hang forever on server crash
# 4. Ignoring channel state; creating a new channel per RPC → huge overhead
# 5. Streaming without flow control → OOM on slow consumers
# 6. Forgetting to call channel.close() → leaking connections
# =============================================================================

