def handle_grpc_error(error: grpc.RpcError) -> None:
    """
    Centralised gRPC error handler. gRPC has its own status code space —
    do NOT map these to HTTP status codes in your business logic.
    """
    code = error.code()          # grpc.StatusCode enum
    details = error.details()    # Human-readable error message
    # error.trailing_metadata() → key-value pairs sent by the server

    # Map of gRPC status codes and what they mean for retry strategy
    RETRY_CODES = {
        grpc.StatusCode.UNAVAILABLE,      # Server is down; retry with backoff
        grpc.StatusCode.RESOURCE_EXHAUSTED,  # Rate limited; retry after delay
        grpc.StatusCode.DEADLINE_EXCEEDED,   # Timeout; consider retry with fresh deadline
    }

    NO_RETRY_CODES = {
        grpc.StatusCode.INVALID_ARGUMENT,   # Bad client data; fix the request
        grpc.StatusCode.NOT_FOUND,          # Resource doesn't exist
        grpc.StatusCode.ALREADY_EXISTS,     # Duplicate; check before creating
        grpc.StatusCode.PERMISSION_DENIED,  # Auth failure; fix credentials
        grpc.StatusCode.UNAUTHENTICATED,    # Missing credentials
        grpc.StatusCode.UNIMPLEMENTED,      # Server doesn't support this method
    }

    if code in RETRY_CODES:
        logger.warning("Retryable gRPC error [%s]: %s", code.name, details)
    elif code in NO_RETRY_CODES:
        logger.error("Non-retryable gRPC error [%s]: %s", code.name, details)
        raise  # Re-raise; caller should not retry
    else:
        logger.error("Unknown gRPC error [%s]: %s", code.name, details)


