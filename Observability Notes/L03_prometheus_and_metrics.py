def run_demo():
    print("=" * 60)
    print("PROMETHEUS METRICS DEMO")
    print("=" * 60)

    metrics = ProductionMetrics()

    # Simulate traffic for 100 requests
    print("\nSimulating 100 HTTP requests...")
    endpoints = ["/api/checkout", "/api/products/{id}", "/api/users/{id}"]
    methods = ["GET", "POST"]
    for i in range(100):
        endpoint = random.choice(endpoints)
        method = "GET" if endpoint != "/api/checkout" else "POST"
        # Simulate latency: checkout is slower
        if endpoint == "/api/checkout":
            duration = random.gauss(0.35, 0.1)
        else:
            duration = random.gauss(0.05, 0.02)
        duration = max(0.001, duration)
        # Simulate 2% error rate
        status = 500 if random.random() < 0.02 else 200
        metrics.record_request(method, endpoint, status, duration)

    # Simulate cache operations (85% hit rate)
    print("Simulating 200 cache lookups...")
    for _ in range(200):
        hit = random.random() < 0.85
        metrics.record_cache_operation("product_cache", hit)
        metrics.cache_operations.labels(
            cache_name="product_cache",
            result="hit" if hit else "miss",
        ).inc()

    # Report P99 latency per endpoint
    print("\n--- P99 Latency by Endpoint ---")
    for endpoint in endpoints:
        normalized = metrics._normalize_endpoint(endpoint)
        p99 = metrics.request_duration.compute_quantile(
            0.99, method="GET", endpoint=normalized
        )
        print(f"  {normalized}: P99 = {p99*1000:.1f}ms")

    # Report cache hit rate
    total_hits = metrics.cache_operations.get_value(
        cache_name="product_cache", result="hit"
    )
    total_misses = metrics.cache_operations.get_value(
        cache_name="product_cache", result="miss"
    )
    total = total_hits + total_misses
    hit_rate = (total_hits / total * 100) if total > 0 else 0
    print(f"\n--- Cache Hit Rate ---")
    print(f"  product_cache: {hit_rate:.1f}% (target: > 90%)")

    # Cardinality validation demo
    print("\n--- Label Cardinality Validation ---")
    bad_labels = ["endpoint", "user_id", "status_code", "request_id"]
    warnings = LabelCardinalityValidator.validate_labels(
        "http_requests_total", bad_labels
    )
    for warning in warnings:
        print(f"  ⚠ {warning[:80]}...")


if __name__ == "__main__":
    run_demo()
