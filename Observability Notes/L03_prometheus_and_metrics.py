# SECTION 8: LABEL CARDINALITY — DETAILED RULES
# ============================================================

class LabelCardinalityValidator:
    """
    Validates that metric labels won't cause cardinality explosion.
    In production: enforce this at code review time.
    """

    # Maximum acceptable cardinality per metric family
    MAX_SERIES_PER_METRIC = 10_000

    # Labels that are ALWAYS forbidden (unbounded)
    FORBIDDEN_LABEL_PATTERNS = [
        "user_id", "customer_id", "account_id",  # millions of users
        "request_id", "trace_id", "session_id",  # one per request
        "ip_address", "ip",                       # millions of IPs
        "timestamp", "date", "time",              # infinite time values
        "url", "path",                            # unparameterized paths
        "query",                                  # query string values
        "email",                                  # PII + unbounded
    ]

    # Labels that are OK (bounded, low cardinality)
    RECOMMENDED_LABELS = {
        "http": ["method", "endpoint", "status_code"],
        # method: GET/POST/PUT/DELETE/PATCH = 5 values
        # endpoint: normalized paths, typically < 50
        # status_code: 200/201/400/401/404/422/429/500/503 ≈ 15 values
        # → max series: 5 × 50 × 15 = 3,750 ✓

        "database": ["operation", "table", "db_name"],
        # operation: select/insert/update/delete = 4
        # table: typically < 30 tables
        # → max series: 4 × 30 = 120 ✓

        "cache": ["cache_name", "result"],
        # cache_name: user_cache, product_cache etc = < 10
        # result: hit/miss = 2
        # → max series: 10 × 2 = 20 ✓

        "queue": ["queue_name", "status"],
        # queue_name: < 20 queues
        # status: success/failure = 2
        # → max series: 20 × 2 = 40 ✓
    }

    @classmethod
    def validate_labels(cls, metric_name: str, labels: list[str]) -> list[str]:
        """Returns list of warnings for problematic label choices."""
        warnings = []

        for label in labels:
            label_lower = label.lower()
            for forbidden in cls.FORBIDDEN_LABEL_PATTERNS:
                if forbidden in label_lower:
                    warnings.append(
                        f"DANGER: label '{label}' in metric '{metric_name}' "
                        f"matches forbidden pattern '{forbidden}'. "
                        f"This will create unbounded time series. "
                        f"Use logs or traces for per-entity debugging."
                    )
                    break

        return warnings


