# ============================================================
# WHAT: Prometheus data model, metric types (Counter/Gauge/Histogram/
#       Summary), Python prometheus_client usage, PromQL queries,
#       label cardinality rules, and production metrics setup for
#       a FastAPI service.
# WHY:  Metrics are the cheapest observability signal — cheap to
#       collect, cheap to store, cheap to query. Prometheus is the
#       de-facto standard for cloud-native metrics. PromQL lets you
#       ask "what is my P99 latency right now?" in one line.
# LEVEL: Advanced
# ============================================================
"""
CONCEPT OVERVIEW:
    Prometheus uses a PULL model: it periodically scrapes a /metrics
    HTTP endpoint on each monitored service. This is different from
    StatsD/Datadog which use a PUSH model (service pushes to collector).

    Pull advantages:
      - Prometheus knows if a service is down (failed scrape = alert)
      - Services don't need to know Prometheus address
      - Easy to test: curl /metrics locally
    Pull disadvantages:
      - Short-lived jobs may complete before scrape (use Pushgateway)
      - Requires network access from Prometheus to each service

    Data model: metric_name{label1="val1", label2="val2"} value timestamp
    One unique label combination = one time series.
    Prometheus stores all time series in a TSDB (Time Series Database)
    on local disk using efficient column-oriented compression.

PRODUCTION USE CASE:
    FastAPI service with automatic instrumentation:
      - prometheus-fastapi-instrumentator auto-adds:
        http_requests_total, http_request_duration_seconds
      - Add custom metrics for: DB pool, cache hit rate, queue depth,
        business metrics (payments_total, orders_by_status)
      - Recording rules precompute expensive aggregations
      - Dashboards in Grafana query Prometheus via PromQL

COMMON MISTAKES:
    1. Using Summary instead of Histogram — Summary computes quantiles
       client-side and cannot be aggregated across instances. If you
       have 3 replicas, you CANNOT compute fleet-wide P99 from Summaries.
       Always use Histogram in production.
    2. High-cardinality labels — labeling with user_id, URL with IDs,
       session_id. Each unique value = one time series. At 1M users:
       1M time series × 3KB = 3GB just for one metric.
    3. Forgetting to normalize URL labels — /users/123 and /users/456
       must both map to /users/{id} or you get per-user time series.
    4. Using .inc() on a Gauge instead of .set() — Gauges should
       represent current state, not increments.
    5. Not using recording rules for expensive queries — histogram_quantile
       over 1M series computed in Grafana on every dashboard load is slow.
"""

