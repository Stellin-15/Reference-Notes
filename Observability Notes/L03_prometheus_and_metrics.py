# SECTION 4: HISTOGRAM
# ============================================================
# HISTOGRAM: measures distribution of observed values using pre-defined
# "buckets". Each observation is counted in all buckets with le >= value.
#
# Auto-generates 3 time series per label combination:
#   metric_bucket{le="0.1"} = count of observations <= 0.1s
#   metric_count              = total observations
#   metric_sum                = sum of all observations
#
# Use for: request duration, response size, DB query time, batch size
#
# Bucket selection: cover the full realistic range.
# Default buckets: .005, .01, .025, .05, .1, .25, .5, 1, 2.5, 5, 10
# For a web API targeting P99 < 200ms, use custom buckets:
#   .001, .005, .01, .025, .05, .1, .25, .5, 1, 2.5
# This gives better resolution around the SLO threshold (0.1s = 100ms)
#
# PromQL to compute P99:
#   histogram_quantile(0.99, rate(http_request_duration_seconds_bucket[5m]))
#
# This works across multiple instances! Unlike Summary.
# Prometheus aggregates all the _bucket series before computing quantile.

class SimulatedHistogram:
    """
    Simulates prometheus_client Histogram.
    In production:
        duration_histogram = Histogram(
            'http_request_duration_seconds',
            'Request duration in seconds',
            ['method', 'endpoint'],
            buckets=[.001, .005, .01, .025, .05, .1, .25, .5, 1, 2.5]
        )
        with duration_histogram.labels(method='POST', endpoint='/checkout').time():
            process_request()
    """

    DEFAULT_BUCKETS = [.005, .01, .025, .05, .1, .25, .5, 1.0, 2.5, 5.0, float('inf')]

    def __init__(
        self,
        name: str,
        description: str,
        labelnames: list[str],
        buckets: Optional[list[float]] = None,
    ):
        self.name = name
        self.description = description
        self.labelnames = labelnames
        # Always ensure +Inf bucket exists (catches all observations)
        self.buckets = sorted(set((buckets or self.DEFAULT_BUCKETS) + [float('inf')]))
        # bucket_counts[(label_key, le)] = count
        self._bucket_counts: dict[tuple, int] = {}
        self._sum: dict[tuple, float] = {}
        self._count: dict[tuple, int] = {}

    def labels(self, **kwargs) -> "SimulatedHistogram._LabeledHistogram":
        key = tuple(kwargs.get(l, "") for l in self.labelnames)
        return self._LabeledHistogram(self, key)

    def _observe(self, label_key: tuple, value: float):
        """
        Record one observation. Increments all buckets where le >= value.
        This is O(buckets) per observation — typically 10-15 buckets.
        """
        for bucket in self.buckets:
            bkey = (label_key, bucket)
            if value <= bucket:
                self._bucket_counts[bkey] = self._bucket_counts.get(bkey, 0) + 1

        self._sum[label_key] = self._sum.get(label_key, 0.0) + value
        self._count[label_key] = self._count.get(label_key, 0) + 1

    def get_bucket_data(self, **kwargs) -> list[tuple[float, int]]:
        """Returns (bucket_le, cumulative_count) pairs for PromQL simulation."""
        key = tuple(kwargs.get(l, "") for l in self.labelnames)
        return [
            (b, self._bucket_counts.get((key, b), 0))
            for b in self.buckets
        ]

    class _LabeledHistogram:
        def __init__(self, parent: "SimulatedHistogram", key: tuple):
            self._parent = parent
            self._key = key

        def observe(self, value: float):
            """Record a single observation (e.g., request duration in seconds)."""
            self._parent._observe(self._key, value)

        @contextmanager
        def time(self):
            """
            Context manager: automatically observes elapsed time in seconds.
            Usage:
                with duration.labels(method='GET', endpoint='/health').time():
                    result = do_work()
            """
            start = time.perf_counter()
            try:
                yield
            finally:
                elapsed = time.perf_counter() - start
                self.observe(elapsed)

    def compute_quantile(self, q: float, **kwargs) -> float:
        """
        Compute approximate quantile from histogram buckets.
        This is what histogram_quantile() does in PromQL.
        Approximation accuracy depends on bucket resolution.
        """
        key = tuple(kwargs.get(l, "") for l in self.labelnames)
        total = self._count.get(key, 0)
        if total == 0:
            return 0.0

        target_count = q * total

        # Find the bucket where cumulative count crosses target
        prev_count = 0
        prev_bound = 0.0

        for bucket in self.buckets:
            bkey = (key, bucket)
            count = self._bucket_counts.get(bkey, 0)

            if count >= target_count:
                # Interpolate within this bucket
                if bucket == float('inf'):
                    return prev_bound
                count_in_bucket = count - prev_count
                if count_in_bucket == 0:
                    return prev_bound
                fraction = (target_count - prev_count) / count_in_bucket
                return prev_bound + fraction * (bucket - prev_bound)

            prev_count = count
            prev_bound = bucket

        return self.buckets[-2] if len(self.buckets) > 1 else 0.0


# ============================================================
# SECTION 5: WHY NOT SUMMARY
# ============================================================
# Summary: computes quantiles on the CLIENT side (in the application).
#
# Problem: if you have 3 replicas and each computes P99 independently,
# you CANNOT combine them into a fleet-wide P99.
#   Replica 1 P99 = 180ms
#   Replica 2 P99 = 220ms
#   Replica 3 P99 = 195ms
#   Fleet P99 = ??? (you don't know — it requires all data)
#
# With Histogram:
#   All three replicas expose bucket counts
#   PromQL sums the buckets: sum(rate(..._bucket[5m])) by (le)
#   histogram_quantile(0.99, ...) computes accurate fleet-wide P99
#
# Summary use case: none in modern systems. Use Histogram always.
# Exception: if you need exact (not approximate) quantiles and only
# have 1 instance — but this is rare.

SUMMARY_HISTOGRAM_COMPARISON = {
    "Histogram": {
        "quantile_accuracy": "Approximate (depends on bucket resolution)",
        "aggregatable": "YES — can sum across replicas",
        "memory": "O(buckets) per label combination",
        "use_when": "Always — the standard choice",
    },
    "Summary": {
        "quantile_accuracy": "Exact (computed from all observations)",
        "aggregatable": "NO — per-instance only",
        "memory": "O(observations in sliding window)",
        "use_when": "Almost never — prefer Histogram",
    },
}


