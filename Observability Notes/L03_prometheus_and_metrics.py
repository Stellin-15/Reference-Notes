# ============================================================
# SECTION 1: PROMETHEUS DATA MODEL
# ============================================================
# Prometheus time series format:
#
# metric_name{label1="val1", label2="val2"} value [timestamp]
#
# Examples:
#   http_requests_total{method="POST",endpoint="/checkout",status_code="200"} 1847
#   http_request_duration_seconds_bucket{endpoint="/checkout",le="0.1"} 1204
#   http_request_duration_seconds_bucket{endpoint="/checkout",le="0.5"} 1799
#   http_request_duration_seconds_bucket{endpoint="/checkout",le="+Inf"} 1847
#   db_pool_connections_active{pool="primary"} 12
#
# Naming conventions:
#   - Use underscores (not dots or dashes)
#   - Append unit: _seconds, _bytes, _total, _ratio
#   - _total suffix: REQUIRED for Counters
#   - _bucket, _count, _sum: auto-generated by Histogram
#   - Base unit: seconds (not milliseconds), bytes (not megabytes)

@dataclass
class PrometheusMetricSpec:
    """Documents a metric's purpose, labels, and usage."""
    name: str
    metric_type: str        # counter | gauge | histogram | summary
    description: str
    labels: list[str]
    unit: str
    example_promql: str


# ============================================================
# SECTION 2: COUNTER
# ============================================================
# COUNTER: monotonically increasing value. NEVER resets (except
# process restart, which Prometheus handles with reset detection).
#
# Use for: total requests, total errors, total bytes sent,
#          total jobs processed, total retries
#
# DO NOT use for: current queue length, memory usage
# (those go up AND down → use Gauge)
#
# Key insight: counters are useless raw. Use rate() in PromQL.
#   raw counter = 1,847,392 requests (meaningless without timeframe)
#   rate(requests_total[5m]) = 47.3 requests/second (actionable)

class SimulatedCounter:
    """
    Simulates prometheus_client Counter behavior.
    In production:
        requests_total = Counter(
            'http_requests_total',
            'Total HTTP requests',
            ['method', 'endpoint', 'status_code']
        )
        requests_total.labels(method='POST', endpoint='/checkout', status_code='200').inc()
    """

    def __init__(self, name: str, description: str, labelnames: list[str]):
        self.name = name
        self.description = description
        self.labelnames = labelnames
        self._values: dict[tuple, float] = {}

    def labels(self, **kwargs) -> "SimulatedCounter._LabeledCounter":
        """Get a labeled version of this counter."""
        key = tuple(kwargs.get(l, "") for l in self.labelnames)
        return self._LabeledCounter(self, key)

    def _inc(self, label_key: tuple, amount: float = 1.0):
        """Increment counter (called by labeled counter)."""
        if amount < 0:
            raise ValueError("Counter cannot be decremented")
        self._values[label_key] = self._values.get(label_key, 0.0) + amount

    def get_value(self, **kwargs) -> float:
        key = tuple(kwargs.get(l, "") for l in self.labelnames)
        return self._values.get(key, 0.0)

    class _LabeledCounter:
        def __init__(self, parent: "SimulatedCounter", key: tuple):
            self._parent = parent
            self._key = key

        def inc(self, amount: float = 1.0):
            self._parent._inc(self._key, amount)


# ============================================================
# SECTION 3: GAUGE
# ============================================================
# GAUGE: current value that can go up or down.
#
# Use for: active connections, queue depth, memory used,
#          cache size, temperature, in-flight requests
#
# Methods: .set(n), .inc(n), .dec(n), .set_to_current_time()
#
# Gauge trap: if process restarts, gauge resets to 0 then increases.
# Prometheus handles this fine for current-state metrics.
# But if you're using a Gauge as a "running total" — use a Counter instead.

class SimulatedGauge:
    """
    Simulates prometheus_client Gauge.
    In production:
        active_connections = Gauge(
            'db_pool_connections_active',
            'Active database connections',
            ['pool_name']
        )
        active_connections.labels(pool_name='primary').set(12)
        active_connections.labels(pool_name='primary').inc()
        active_connections.labels(pool_name='primary').dec()
    """

    def __init__(self, name: str, description: str, labelnames: list[str]):
        self.name = name
        self.description = description
        self.labelnames = labelnames
        self._values: dict[tuple, float] = {}

    def labels(self, **kwargs) -> "SimulatedGauge._LabeledGauge":
        key = tuple(kwargs.get(l, "") for l in self.labelnames)
        return self._LabeledGauge(self, key)

    class _LabeledGauge:
        def __init__(self, parent: "SimulatedGauge", key: tuple):
            self._parent = parent
            self._key = key

        def set(self, value: float):
            """Set to an absolute value — use for polling current state."""
            self._parent._values[self._key] = value

        def inc(self, amount: float = 1.0):
            """Increment — use for tracking active requests."""
            current = self._parent._values.get(self._key, 0.0)
            self._parent._values[self._key] = current + amount

        def dec(self, amount: float = 1.0):
            current = self._parent._values.get(self._key, 0.0)
            self._parent._values[self._key] = current - amount

        @contextmanager
        def track_inprogress(self):
            """
            Context manager to track in-flight operations.
            Increments on enter, decrements on exit (even on exception).
            In production: use prometheus_client's track_inprogress().
            """
            self.inc()
            try:
                yield
            finally:
                self.dec()


