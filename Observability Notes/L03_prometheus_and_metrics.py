# SECTION 6: PRODUCTION METRICS FOR A FastAPI SERVICE
# ============================================================

class ProductionMetrics:
    """
    Complete metrics setup for a production FastAPI service.
    Demonstrates all metric types with correct usage patterns.

    In production (FastAPI + prometheus-fastapi-instrumentator):

        from prometheus_fastapi_instrumentator import Instrumentator

        app = FastAPI()

        # Auto-instruments: http_requests_total, http_request_duration_seconds
        Instrumentator().instrument(app).expose(app)

    Then add custom metrics below for business logic.
    """

    def __init__(self):
        # ---- HTTP metrics (auto-instrumented in production) ----
        self.requests_total = SimulatedCounter(
            "http_requests_total",
            "Total HTTP requests by method, endpoint, and status",
            ["method", "endpoint", "status_code"],
        )
        self.request_duration = SimulatedHistogram(
            "http_request_duration_seconds",
            "HTTP request duration in seconds",
            ["method", "endpoint"],
            # Custom buckets: fine resolution around SLO threshold (100ms)
            buckets=[.001, .005, .01, .025, .05, .1, .2, .5, 1.0, 2.5, 5.0],
        )
        self.active_requests = SimulatedGauge(
            "http_requests_active",
            "Currently processing HTTP requests",
            ["endpoint"],
        )

        # ---- Database metrics ----
        self.db_pool_active = SimulatedGauge(
            "db_pool_connections_active",
            "Active connections currently checked out from pool",
            ["pool_name"],
        )
        self.db_pool_size = SimulatedGauge(
            "db_pool_connections_total",
            "Total connections in pool (active + idle)",
            ["pool_name"],
        )
        self.db_query_duration = SimulatedHistogram(
            "db_query_duration_seconds",
            "Database query execution time",
            ["operation", "table"],  # operation: select|insert|update|delete
            buckets=[.001, .005, .01, .025, .05, .1, .5, 1.0, 5.0],
        )

        # ---- Cache metrics ----
        self.cache_operations = SimulatedCounter(
            "cache_operations_total",
            "Cache operations by result (hit or miss)",
            ["cache_name", "result"],  # result: hit | miss
        )

        # ---- Queue metrics ----
        self.queue_depth = SimulatedGauge(
            "message_queue_depth",
            "Number of messages waiting in queue",
            ["queue_name"],
        )
        self.queue_processed = SimulatedCounter(
            "queue_messages_processed_total",
            "Messages processed from queue",
            ["queue_name", "status"],  # status: success | failure
        )

        # ---- Business metrics ----
        self.orders_total = SimulatedCounter(
            "business_orders_total",
            "Orders placed by status",
            ["status", "payment_method"],  # status: success|failed|refunded
        )
        self.revenue_total = SimulatedCounter(
            "business_revenue_cents_total",
            "Revenue collected in cents",
            ["currency", "payment_method"],
        )

    def record_request(
        self,
        method: str,
        endpoint: str,
        status_code: int,
        duration_seconds: float,
    ):
        """Record metrics for one completed HTTP request."""
        # Normalize endpoint: /users/123 → /users/{id}
        # Real: use regex patterns or route matching
        normalized_endpoint = self._normalize_endpoint(endpoint)

        self.requests_total.labels(
            method=method,
            endpoint=normalized_endpoint,
            status_code=str(status_code),
        ).inc()

        self.request_duration.labels(
            method=method,
            endpoint=normalized_endpoint,
        ).observe(duration_seconds)

    def record_db_query(self, operation: str, table: str, duration_seconds: float):
        """Record metrics for one database query."""
        self.db_query_duration.labels(
            operation=operation,
            table=table,
        ).observe(duration_seconds)

    def record_cache_operation(self, cache_name: str, hit: bool):
        """Record cache hit or miss."""
        result = "hit" if hit else "miss"
        self.cache_operations.labels(
            cache_name=cache_name,
            result=result,
        ).inc()

    def record_order(
        self,
        status: str,
        payment_method: str,
        amount_cents: int,
        currency: str = "USD",
    ):
        """Record business metrics for an order."""
        self.orders_total.labels(
            status=status,
            payment_method=payment_method,
        ).inc()

        if status == "success":
            self.revenue_total.labels(
                currency=currency,
                payment_method=payment_method,
            ).inc(amount_cents)

    def _normalize_endpoint(self, path: str) -> str:
        """
        Replace dynamic path segments with placeholders.
        CRITICAL: prevents high-cardinality time series.
        /users/123 → /users/{id}
        /orders/abc-def → /orders/{order_id}
        """
        import re
        # Replace UUIDs: 8-4-4-4-12 hex pattern
        path = re.sub(
            r'[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}',
            '{uuid}', path
        )
        # Replace numeric IDs
        path = re.sub(r'/\d+', '/{id}', path)
        return path


