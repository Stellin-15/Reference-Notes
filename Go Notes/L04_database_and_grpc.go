// NewGRPCClient creates a gRPC client connection to the user service.
// grpc.NewClient does NOT establish the TCP connection immediately;
// it dials lazily on the first RPC. Use WithBlock() + timeout if you
// need to verify connectivity at startup.
func NewGRPCClient(addr string) (*grpc.ClientConn, error) {
	conn, err := grpc.NewClient(
		addr, // e.g. "user-service:50051" (K8s DNS name)
		grpc.WithInsecure(), // dev only — use grpc.WithTransportCredentials in prod
	)
	if err != nil {
		return nil, fmt.Errorf("dial %s: %w", addr, err)
	}
	return conn, nil
}

