// ============================================================
// L04: Database Access and gRPC in Go
// ============================================================
// WHAT: pgxpool connection pool, sqlc type-safe queries,
//       database transactions, proto3 service definitions,
//       gRPC server/client setup, and unary interceptors.
// WHY:  Go's database/sql is the lowest-level interface; pgx/v5
//       is the idiomatic PostgreSQL driver. gRPC over HTTP/2
//       gives you strongly-typed, generated client/server stubs,
//       streaming, and efficient binary serialisation — all
//       without hand-writing JSON marshalling.
// LEVEL: Intermediate
// ============================================================
/* CONCEPT OVERVIEW:
   database/sql is the standard library interface. Any driver
   (pgx, lib/pq) registers with it. pgx/v5 also provides its
   own native API (pgxpool) that bypasses database/sql for
   better performance and PostgreSQL-specific features.

   pgxpool is a thread-safe connection pool. Connections are
   borrowed for a query and returned automatically. Pool sizing:
   MaxConns should be set so that (instances × MaxConns) does
   not exceed PostgreSQL's max_connections (default 100).
   A rule of thumb: MaxConns = max_connections / app_instances.

   sqlc generates type-safe Go code from .sql query files.
   You write SQL; sqlc produces a Go function with the exact
   input/output types. No ORM magic, no reflection at runtime,
   compile-time errors for schema mismatches.

   Transactions: begin → deferred rollback → do work → commit.
   The deferred rollback is a no-op if Commit succeeds; it only
   fires on early return or panic, guaranteeing no partial writes.

   gRPC: define messages and services in .proto files. Run
   protoc to generate Go interfaces. Implement the interface
   on a struct. Register with grpc.NewServer. Interceptors
   wrap every call (logging, auth, recovery) — equivalent to
   HTTP middleware.

   PRODUCTION USE CASE:
   Internal microservices communicate via gRPC (binary, fast,
   contract-first). Public-facing API uses a gRPC-Gateway to
   translate REST/JSON → gRPC. The user-service uses pgxpool
   for DB access, sqlc for type-safe queries, and a transaction
   for atomic fund transfers.

   COMMON MISTAKES:
   1. Forgetting rows.Close() — every open rows object holds a
      connection from the pool; if you don't close it the pool
      drains and new queries block forever.
   2. Not using context with timeout — a runaway DB query or
      unresponsive gRPC peer blocks the goroutine indefinitely.
      Always derive a context with deadline from the incoming ctx.
   3. Changing proto field numbers — field numbers are permanent.
      Changing them breaks all existing serialised data and every
      deployed client. Add new fields with new numbers; never
      reuse or change existing ones.
   4. Ignoring tx.Rollback() error — deferred rollback after a
      successful commit returns ErrTxDone, which is harmless and
      expected; don't log it as an error.
   5. MaxConns too high — each PostgreSQL connection uses ~5 MB
      of RAM. 100 app pods × 50 MaxConns = 5 000 connections =
      25 GB RAM on the DB server. Size carefully. */

