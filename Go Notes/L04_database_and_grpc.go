// ===========================================================================
// gRPC SERVER — wiring interceptors and starting the listener
// ===========================================================================

// StartGRPCServer creates and starts the gRPC server.
// Interceptors are chained in order: Recovery → Auth → Logging → Handler.
// The outermost interceptor (listed first in ChainUnaryInterceptor) runs first.
func StartGRPCServer(pool *pgxpool.Pool) error {
	// Chain multiple interceptors — executed in declaration order.
	srv := grpc.NewServer(
		grpc.ChainUnaryInterceptor(
			RecoveryInterceptor, // must be outermost to catch panics in auth/logging
			AuthInterceptor,
			LoggingInterceptor,
		),
	)

	userSvc := &UserServiceServer{queries: &Queries{pool: pool}}
	// In a real project: pb.RegisterUserServiceServer(srv, userSvc)
	_ = userSvc // suppress unused warning in this standalone file

	// TCP listener on all interfaces — in K8s, the Service routes to this port.
	lis, err := net.Listen("tcp", ":50051")
	if err != nil {
		return fmt.Errorf("listen: %w", err)
	}

	slog.Info("gRPC server listening", "addr", lis.Addr())
	// Serve blocks until the server is stopped (or an error occurs).
	return srv.Serve(lis)
}

