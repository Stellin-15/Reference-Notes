func main() {
	// Read DSN from env — never hardcode credentials.
	dsn := os.Getenv("DATABASE_URL")
	if dsn == "" {
		log.Fatal("DATABASE_URL env var is required")
	}

	ctx := context.Background()

	// Create the connection pool; fail fast if DB is unreachable.
	pool, err := NewPool(ctx, dsn)
	if err != nil {
		log.Fatalf("connect to db: %v", err)
	}
	defer pool.Close() // return all connections to PostgreSQL on shutdown

	// Start the gRPC server (blocks until error or signal).
	if err := StartGRPCServer(pool); err != nil {
		log.Fatalf("gRPC server: %v", err)
	}
}
